function detectBrowserLanguage(): Language {
  // Check if we're in a browser environment
  if (typeof window === "undefined" || typeof navigator === "undefined") return "fr"

  try {
    const browserLang = navigator.language.split("-")[0].toLowerCase()
    const supportedLanguages: Language[] = ["fr", "en", "de", "es", "it", "pt", "nl"]

    return supportedLanguages.includes(browserLang as Language) ? (browserLang as Language) : "fr"
  } catch (error) {
    console.error("[v0] Error detecting browser language:", error)
    return "fr"
  }
}

